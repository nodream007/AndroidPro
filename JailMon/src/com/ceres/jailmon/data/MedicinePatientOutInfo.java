package com.ceres.jailmon.data;


public class MedicinePatientOutInfo {
	private String Name;
	private String ApplyTime;
	private String MedicalTime;
	private String IllNessExp;
	private String IllNessType;
	private String OutExplain;
	private String DoctorName;
	private String LeadName;
	private String LeadTime;
	private String LeadOpinion;
	private String Remark;

	public String getName() {
		return Name;
	}

	public void setName(String name) {
		Name = name;
	}

	public String getApplyTime() {
		return ApplyTime;
	}

	public void setApplyTime(String applyTime) {
		ApplyTime = applyTime;
	}

	public String getMedicalTime() {
		return MedicalTime;
	}

	public void setMedicalTime(String medicalTime) {
		MedicalTime = medicalTime;
	}

	public String getIllNessExp() {
		return IllNessExp;
	}

	public void setIllNessExp(String illNessExp) {
		IllNessExp = illNessExp;
	}

	public String getIllNessType() {
		return IllNessType;
	}

	public void setIllNessType(String illNessType) {
		IllNessType = illNessType;
	}

	public String getOutExplain() {
		return OutExplain;
	}

	public void setOutExplain(String outExplain) {
		OutExplain = outExplain;
	}

	public String getDoctorName() {
		return DoctorName;
	}

	public void setDoctorName(String doctorName) {
		DoctorName = doctorName;
	}

	public String getLeadName() {
		return LeadName;
	}

	public void setLeadName(String leadName) {
		LeadName = leadName;
	}

	public String getLeadTime() {
		return LeadTime;
	}

	public void setLeadTime(String leadTime) {
		LeadTime = leadTime;
	}

	public String getLeadOpinion() {
		return LeadOpinion;
	}

	public void setLeadOpinion(String leadOpinion) {
		LeadOpinion = leadOpinion;
	}

	public String getRemark() {
		return Remark;
	}

	public void setRemark(String remark) {
		Remark = remark;
	}

}
