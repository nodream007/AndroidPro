package com.ceres.jailmon.data;

public class MedicineHealthyInfo {
	public String id;
	String HealthyTime;
	String HealthyType;
	String Height;
	String Weight;
	String BloodType;
	String BloodPre;
	String PoliceName;
	String MyMedHis;
	String FamilyMedHis;
	String DoctorSug;
	String Remark;

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public String getHealthyTime() {
		return HealthyTime;
	}

	public void setHealthyTime(String healthyTime) {
		HealthyTime = healthyTime;
	}

	public String getHealthyType() {
		return HealthyType;
	}

	public void setHealthyType(String healthyType) {
		HealthyType = healthyType;
	}

	public String getHeight() {
		return Height;
	}

	public void setHeight(String height) {
		Height = height;
	}

	public String getWeight() {
		return Weight;
	}

	public void setWeight(String weight) {
		Weight = weight;
	}

	public String getBloodType() {
		return BloodType;
	}

	public void setBloodType(String bloodType) {
		BloodType = bloodType;
	}

	public String getBloodPre() {
		return BloodPre;
	}

	public void setBloodPre(String bloodPre) {
		BloodPre = bloodPre;
	}

	public String getPoliceName() {
		return PoliceName;
	}

	public void setPoliceName(String policeName) {
		PoliceName = policeName;
	}

	public String getMyMedHis() {
		return MyMedHis;
	}

	public void setMyMedHis(String myMedHis) {
		MyMedHis = myMedHis;
	}

	public String getFamilyMedHis() {
		return FamilyMedHis;
	}

	public void setFamilyMedHis(String familyMedHis) {
		FamilyMedHis = familyMedHis;
	}

	public String getDoctorSug() {
		return DoctorSug;
	}

	public void setDoctorSug(String doctorSug) {
		DoctorSug = doctorSug;
	}

	public String getRemark() {
		return Remark;
	}

	public void setRemark(String remark) {
		Remark = remark;
	}

}
